import React, { Component } from 'react';
import './App.css';
import Colorsquare from './Colorsquare'
import { Grid, Row } from 'react-bootstrap';

class App extends Component {
  constructor(props){
    super(props)
    
    const boxColors = []

    for(var i=0; i<24; i++){
      boxColors.push({
        id: i, 
        color: this.getRandomColor()
      })
    }
    
    this.state = {boxColors}

  }

  getRandomColor(){
    var randomNumber = Math.floor(Math.random()*this.props.allColors.length)
    return this.props.allColors[randomNumber]
  }

  handleChange(idx){
    const {boxColors} = this.state
    let newboxColors = boxColors.slice(0, idx)
    newboxColors.push({id: idx, color: this.getRandomColor()})
    newboxColors = newboxColors.concat(boxColors.slice(idx + 1))
    this.setState({
      boxColors: newboxColors
    })
  }

  render() {
    let box = this.state.boxColors.map(color => (
      <Colorsquare 
        changeColor = {this.handleChange.bind(this, color.id)}
        color = {color.color}
        key = {color.id}
      />
    ))
    return (
      <div className="App">
        <Grid>
          <Row className = 'show-grid'>
            {box}
          </Row>
        </Grid>
      </div>
    );
  }
}

