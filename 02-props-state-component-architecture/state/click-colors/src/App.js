import React, { Component } from "react";
import Box from "./Box";
import "./App.css";

class App extends Component {
  constructor(props) {
    super(props);
    const numBoxes = 24;
    const boxes = [];
    for (let i = 0; i < numBoxes; i++) {
      boxes.push({
        id: i,
        backgroundColor: this.getRandomColor()
      });
    }
    this.state = { boxes };
    this.handleClick = this.handleClick.bind(this);
  }

  getRandomColor() {
    let index = Math.floor(Math.random() * this.props.colors.length);
    console.log(index);
    return this.props.colors[index];
  }

  handleClick(e) {
    const newBoxes = [...this.state.boxes];
    newBoxes[e.target.id] = {
      id: e.target.id,
      backgroundColor: this.getRandomColor()
    };
    this.setState({ boxes: newBoxes });
  }

  render() {
    const boxes = this.state.boxes.map(box => {
      return (
        <Box
          key={box.id}
          id={box.id}
          backgroundColor={box.backgroundColor}
          handleClick={this.handleClick}
        />
      );
    });
    return (
      <div className="App">
        <header className="App-header">
          <h1 className="App-title">Welcome to Click Colors!</h1>
        </header>
        <p className="App-intro">
          To get started, click a box to change its color.
        </p>
        {boxes}
      </div>
    );
  }
}
